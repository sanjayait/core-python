# # # Method resolution order---->
# It is an order in which python will search there class/instance/method/function.

# # # Method over riding
# if we create same method/funtion in number of class so python we execute them according to there
# Method resolution order.

# #Breif example
# class phone:
#     -------------
#     -------------
#     def full_name(self):
#         ---------

# class Smartphone(phone):
#     ----------------
#     ----------------
#     def full_name(self):
#         ---------

# if we print(full_name) it will execute in smartphone first by method resolution order
# it will over ride phone class()------Smartphone class()        

